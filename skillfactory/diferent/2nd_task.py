# Исходный текст
text = """//А//Роман//А//1.0 ноутбук//А//Пётр//А//12.25 планшет//А//Иван Иванов//А//1.256 компьютер //А//Саша и Маша//А// 15.1 телефон"""

# Разделяем текст по разделителю
parts = text.split('//А//')

# Очищаем список от пустых строк и пробелов
parts = [part.strip() for part in parts if part.strip()]

# Создаем итоговый список
result = []

# Проходим по частям с шагом 2
for i in range(0, len(parts), 2):
    # Берем имя и значение
    name = parts[i]
    value = parts[i+1].split()[0]  # Берем только число, игнорируя остальное
    result.append((name, value))

print(result)


